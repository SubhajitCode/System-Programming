.MODEL SMALL
.STACK 100H
.DATA
    B DB 25,10,30,80,40,50
    MSG1 DB "INPUT ARRAY",0DH,0AH,'$'
    MSG2 DB "SORTED ARRAY",0DH,0AH,'$'
.CODE
    MOV AX,@DATA
    MOV DS,AX
    MOV ES,AX
    LEA SI,B
    MOV CX,6
    MOV AH,9
    LEA DX,MSG1
    INT 33
    CALL PRINT_ARRAY
    CALL SORT
    MOV AH,9
    LEA DX,MSG2
    INT 33
    CALL PRINT_ARRAY
    MOV AH,76
    INT 33
INCLUDE D:\PRINT_A.ASM

SORT PROC
;INPUTS
;SI POINTING AT INPUT ARRAY
;DI POINTING AT INPUT ARRAY
;CX NUMBER OF ELEMENT
;PUSHING START
    PUSH SI 
    PUSH CX
;PUSHING END
    DEC CX
SL1:
    CALL FIND_MIN
    CALL SWAP
    INC SI
    LOOP SL1 

;POPING START
    POP CX
    POP SI
;POPING END
    RET
SORT ENDP

FIND_MIN PROC
;SI POINTS THE INPUT OF THE ARRAY ADDRESS
;CX POINTS THE NUMBER OF ITERATION
;DI CONTAINS INDEX OF MIN
;BX CONTAINS OFFSET
    PUSH SI
    PUSH CX
    PUSH DX
    MOV DI,SI ; [DI] TO CONTAIN MINIMUM VALUE 
F2:
    INC SI
    MOV DH,[SI]
    CMP [DI],DH
    JL F1
    MOV DI,SI
F1:
    LOOP  F2
F3: POP DX
    POP CX
    POP SI
    RET
FIND_MIN ENDP

SWAP PROC
    ;SI CONTAINS THE FIRST VALUE 
    ;DI CONTAINS THE LAST VALUE
    PUSH DX
    MOV DL,[SI]
    XCHG DL,[DI]
    MOV [SI],DL
    POP DX
    RET
SWAP ENDP
END


