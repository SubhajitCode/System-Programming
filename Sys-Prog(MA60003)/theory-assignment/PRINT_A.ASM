PRINT_ARRAY PROC
;SI POINTS AT THE STARTING OF THE DECIMAL ARRAY
;CX CONTAINTS DECIMAL ARRAY LENGTH
    PUSH SI
    PUSH AX
    PUSH CX
    PUSH DX
PL1:
    XOR AX,AX
    MOV AL,[SI]
    CALL OUTDEC
    MOV AH,2
    MOV DL,','
    INT 33
    INC SI
        LOOP PL1
    MOV AH,2
    MOV DL,0DH
    INT 33
    MOV DL,0AH
    INT 33
    POP DX
    POP CX
    POP AX
    POP SI
    RET 
PRINT_ARRAY ENDP
INCLUDE D:\outdec.asm