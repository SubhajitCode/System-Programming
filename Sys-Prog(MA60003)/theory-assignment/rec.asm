.MODEL SMALL
.DATA
	ARRAY DB 5,10,15,16,17,18
.STACK 100H
.CODE
MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	MOV ES,AX
	LEA SI,ARRAY
	MOV BL,0
	MOV BH,5
	PUSH BX
	XOR DX,DX
	CALL SUM
	MOV AH,2
	INT 21H
	MOV AH,4CH
	INT 33
MAIN ENDP

SUM PROC
		PUSH BP
		MOV BP,SP
		MOV BX,WORD PTR[BP+4]
		XOR CX,CX
		ADD CL,BH
		ADD CL,BL
		MOV AL,CL
		MOV CL,2
		DIV CL
		CMP BL,AL
		JNE @ELSE
			MOV BH,0
			PUSH SI
			ADD SI,BX
			ADD DX,[SI]
;			MOV AH,2
;			INT 33
			POP SI
			POP BP
			RET 2
@ELSE:
		MOV AH,BH
		MOV BH,AL
		PUSH BX
		CALL SUM
		MOV BH,AH
		MOV BL,AL
		PUSH BX
		CALL SUM
		POP BP
		RET 2
SUM ENDP
END MAIN